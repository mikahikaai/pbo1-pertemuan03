package branch;

public class Switch {
    public static void main(String[] args) {
        int bini = 4;
        switch (bini){
            case 1:
                System.out.println("Alhamdulillah satu aja gen dlu, satu aja kada habis");
                break;
            case 2:
                System.out.println("Alhamdulillah mun tulak jauh bini sudah ada kawannya");
                break;
            case 3:
                System.out.println("Alhamdulillah mun bekelahi ada nang melerai");
            case 4:
                System.out.println("Alhamdulillah kesampaian jua meumpati sunah rasul");
            default:
                System.out.println("Handak nambah pulang, bini membawa parang");
        }
    }
}
